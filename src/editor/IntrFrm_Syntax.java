    private void btn_LL1_SimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_LL1_SimActionPerformed
        // TODO add your handling code here:        
        
        
        String str=textF_LL1_str.getText(),tmp;
        if(str.contains("$"))
        {
            CATModeler.setMessages("Grammar | LL(1) Simulation - '$' not allowed!", "", 100);
            return;
        }
        
        CATModeler.setMessages("Grammar | Simulating LL(1) with input String : "+str+" ...", "", 100);
        
        ArrayList<Token> inputBuff = new ArrayList<Token>();
        StringTokenizer st = new StringTokenizer(str);        
        while(st.hasMoreTokens())
        { 
            tmp=st.nextToken();
            
            if(tmp.startsWith("id"))   //only id as filter
            {
                inputBuff.add(new Token("", tmp, 0));                
            }
            else
            {
                inputBuff.add(new Token(tmp, "", 0));
            }
        }
        
        try
        {
        parseResultLL1 =grammar.ll1parsingAlgo(inputBuff, table_LL1_Sim);
        //System.out.println("RESULT : "+result);
        
        if(parseResultLL1.message.startsWith("Accepted"))
        { 
            textA_LL1_simMsg.setForeground(new Color(7, 131, 45)); 
            btn_LL1_tree.setEnabled(true);
        }
        else
        { 
            textA_LL1_simMsg.setForeground(Color.red); 
            btn_LL1_tree.setEnabled(false);
        }
        
        textA_LL1_simMsg.setText(parseResultLL1.message);
        
        CATModeler.setMessages("Grammar | LL(1) Simulation Done!", "", 100);
        
        }catch(Exception e)
        {
            Dialog_ExceptionHandler.autoPopJOptionPane(this, "Exception occured!", "Unidentified exception occured while simulating LL1!", e, JOptionPane.ERROR_MESSAGE);
            CATModeler.setMessages("Grammar | LL(1) Simulation Error!", "Simulation Error!", 100);
        }
        /*
        System.out.println("inputBff : ");
        for(int i=0;i<inputBuff.size();i++)
        {
            System.out.print(inputBuff.get(i).toUseStr+ " ");
        }
        */
        
        
    }//GEN-LAST:event_btn_LL1_SimActionPerformed

