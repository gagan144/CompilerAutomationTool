    private void btn_SLR_SimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SLR_SimActionPerformed
        // TODO add your handling code here:
        String str=textF_SLR_str.getText(),tmp;
        if(str.contains("$"))
        {
            CATModeler.setMessages("Grammar | SLR Simulation - '$' not allowed!", "", 100);
            return;
        }
        
        CATModeler.setMessages("Grammar | Simulating SLR with input String : "+str+" ...", "", 100);
        
        ArrayList<Token> inputBuff = new ArrayList<Token>();
        StringTokenizer st = new StringTokenizer(str);        
        while(st.hasMoreTokens())
        { 
            tmp=st.nextToken();
            
            if(tmp.startsWith("id"))   //only id as filter
            {
                inputBuff.add(new Token("", tmp, 0));                
            }
            else
            {
                inputBuff.add(new Token(tmp, "", 0));
            }
        }
        
        
        try
        {
            parseResultSLR =grammar.topDwnParsingAlgo(Grammar.SLR, inputBuff, table_SLR_Sim);
        }catch(Exception e)
        { parseResultSLR=new ParsingReturn(e.getMessage()); }
        
        //System.out.println("RESULT : "+parseResultSLR);
        
        if(parseResultSLR.message.startsWith("Accepted"))
        { 
            textA_SLR_simMsg.setForeground(new Color(7, 131, 45)); 
            btn_SLR_tree.setEnabled(true);
        }
        else
        { 
            textA_SLR_simMsg.setForeground(Color.red); 
            btn_SLR_tree.setEnabled(false);
        }
        
        textA_SLR_simMsg.setText(parseResultSLR.message);
        
        CATModeler.setMessages("Grammar | SLR Simulation Done!", "", 100);
        
    }//GEN-LAST:event_btn_SLR_SimActionPerformed

