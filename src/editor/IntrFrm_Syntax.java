    private void displayFirstFollowOnTable(HashMap<String,HashSet<String>> allFirsts, HashMap<String,HashSet<String>> allFollow)
    {
        //clear table
        DefaultTableModel model=(DefaultTableModel)jTable_Frst_Flw.getModel();
        for( int i = model.getRowCount() - 1; i >= 0; i-- )
        {   
            model.removeRow(i);
        }
        
        if(allFirsts==null || allFollow==null)
        {
            JOptionPane.showMessageDialog(this, "Error evaluating First & Follow.\nPossibly a recursive grammar! ", "Invalid Grammar...", JOptionPane.ERROR_MESSAGE);
            return;
        }
                
        //add to table
        Object rowObj[]=null;
        for (Iterator<Map.Entry<String, HashSet<String>>> it = allFirsts.entrySet().iterator(); it.hasNext(); ) 
        {
            Map.Entry<String, HashSet<String>> entry = it.next();
            String NT = entry.getKey();
            
            //first
            HashSet<String> firstSet = entry.getValue();
            String fisrtStr="";
            
            for(Object i : firstSet.toArray())
            {
                fisrtStr+=i.toString()+" , ";
            }
            if(fisrtStr.length()>=3)
            { fisrtStr=fisrtStr.substring(0, fisrtStr.length()-3); }
                
            //follow
            HashSet<String> followSet = allFollow.get(NT);
            String followStr="";
            
            for(Object i : followSet.toArray())
            {
                followStr+=i.toString()+" , ";
            }
            if(followStr.length()>=3)
            { followStr=followStr.substring(0, followStr.length()-3); }
            
            
            rowObj = new Object[]{NT,fisrtStr,followStr};
            model.insertRow(jTable_Frst_Flw.getRowCount(), rowObj);        
            //System.out.println(key+" -> "+value );
        }
        
               
        
    }
    
