    public boolean chkAndLoadToObject()
    {
        //check extensions
        String exts[],tmp="";
        try
        {            
            StringTokenizer st = new StringTokenizer(textF_exts.getText(), ",");
            exts=new String[st.countTokens()];
            if(exts.length==0)
            {
                CATModeler.setMessages("General Properties | Please enter file extension(s) supported by the language you are trying to model!", "Error", 0);
                return false;
            }
            int i=0;
            while(st.hasMoreTokens())
            {                
                tmp=st.nextToken();
                if(!chkExtString(tmp))
                { return false; }
                else 
                {
                    //System.out.println(tmp);  
                    exts[i++]=tmp;
                }
            }            
            
        }catch(Exception e)
        { System.out.println(e); return false; }        
        
        
        int prior[]=new int[14];
        for(int i=0;i<14;i++)
        {
            prior[i]=(Integer)(table_mdlPrior.getModel().getValueAt(i,0));
            //System.out.println(prior[i]);
        }
        
        CATModeler.data.addProperty(textF_prgmLang.getText(), textF_mdl.getText(), textF_auth.getText(), exts, textA_descp.getText(),prior);        
        return true;
    }
    
